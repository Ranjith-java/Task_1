public class Solution1{

    public static void main(String[] agrs){

        int i=10,j=50;

        System.out.println("Number Range Printed using for loop");

        //number range printed using for loop

        for(i=10;i<=50;i++){

            System.out.print(i+" ");

        }

        System.out.println("");

        //number range printed using while loop

        i=10;

        System.out.println("number range printed using while loop");

        while(i<=j){

            System.out.print(i+" ");

            i++;

        }

    }

}

